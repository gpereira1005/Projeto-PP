    private AbstractTableModel criarTabela() {
        String[] nomeColunas = {"Codigo", "Morada", "Localidade", "Gestor"};

        return new AbstractTableModel() {
            @Override
            public String getColumnName(int column) {
                return nomeColunas[column];
            }

            @Override
            public int getRowCount() {
                //Retorna o número de linhas que a tabela deverá ter
                return sistema.getCentros_Vacinacao().calcularNumeroCentro_Vacinacao();
            }

            @Override
            public int getColumnCount() {
                //Retorna o número de colunas que a tabela deverá ter
                return nomeColunas.length;
            }

            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                /*
                Este método é invocado quando se pretende "popular" cada uma das células da tabela
                Se a tabela tem 3 linhas e 2 colunas existem 6 células (3*2), logo o método será invocado 6 vezes
                    rowIndex representa a linha da célula (0 a rowCount -1)
                    columnIndex representa a coluna da célula (0 a ColumnCount -1)
                 */ CentroVacinacao c = sistema.getCentros_Vacinacao().verCentros().get(rowIndex);
                switch (columnIndex) {
                    case 0:
                        return c.getCodigo();
                    case 1:
                        return c.getMorada();
                    case 2:
                        return c.getLocalidade();
                    case 3:
                        return c.getGestor().getNome();
                    default:
                        return "";
                }
            }
        };
    }

    private void atualizar() {
        modeloTabela.fireTableDataChanged();
    }

