    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int rowIndex = tabelaCentros.getSelectedRow();
        if (rowIndex == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma centro para editar");
            return;
        }
        String codigo = (String) modeloTabela.getValueAt(rowIndex, 0);
        CentroVacinacao c = sistema.getCentros_Vacinacao().procurarCentro_Vacinacao(codigo);
        dispose();
        JanelaAdmListaCentroAddCentro janela = new JanelaAdmListaCentroAddCentro(sistema, dados, 1, c);
        janela.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

